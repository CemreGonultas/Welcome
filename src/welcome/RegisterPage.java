    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String username = kullanici_adi_alani.getText();
        String password = new String(parola_alani.getPassword());
        String name = isim_alani.getText();
        String surname = soyadi_alani.getText();
        String city = sehir_alani.getText();
        String country = ulke_alani.getText();
        String mail = mail_alani.getText();
        
        ArrayList<User> user_list = LoginPage.getUser_list();
        user_list.add(new User(username,password));
        
        if(name.isEmpty() || surname.isEmpty() || city.isEmpty() || country.isEmpty() || mail.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please fill in the blank fields!");
        }else{
            JOptionPane.showMessageDialog(this,"Registration successful...\n Press ok to log in...");
            LoginPage loginPage = new LoginPage();
            loginPage.setVisible(true);
            setVisible(false);
        }
        
        islemler.kullaniciEkle(name,surname,city,country,mail,username,password);
    
        
        
 
       


    }//GEN-LAST:event_registerActionPerformed

